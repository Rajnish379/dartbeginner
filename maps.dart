// Collection

void main() {
  // Map

// String String map
  var gifts = {
    // Key: Value
    'first' : 'partridge',
    'second' : 'turtledoves',
    'fifth' : 'golden rings'
  };

  print(gifts['fifth']);

// Int String map
  var g = {
     1 : 'partridge',
    2 : 'turtledoves',
    5 : 'golden rings'
  };

  print(g[1]);

  var x = Map();
  x['first'] = 'Mango';
  print(x['first']);
}